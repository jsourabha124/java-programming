
public class MathsOperations {
	static void main(String args[]) {
		/*
		Perform the following operations on 6 and 3 and print their respective outputs in different lines.
		1. Addition
		2. Subtraction
		3. Multiplication
		4. Division
		*/
		//Addition
		System.out.println(6+3);
		//Subtraction
		System.out.println(6-3);
		//Multiplication
		System.out.println(6*3);
		//Division
		System.out.println(6/3);
	}
}
